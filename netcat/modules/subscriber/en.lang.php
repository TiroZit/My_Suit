function day_of_week_name($day) {
    $day_names = array(1=>'Monday',
            2 => 'Tuesday',
            3 => 'Wednesday',
            4 => 'Thursday',
            5 => 'Friday',
            6 => 'Saturday',
            0 => 'Sunday');
    return $day_names[$day];
}

